const wantedIds = new Set(process.argv.slice(2));
const targets = sections.filter((s) => s.id !== "01-hero" && (wantedIds.size === 0 || wantedIds.has(s.id)));

for (const section of targets) {
  const jpgName = section.imageName.replace(/\.png$/i, ".jpg");
  const mobileRef = path.join(mobileDir, jpgName);
  const outPath = path.join(imageDir, jpgName);

  console.log(`generate ${section.id} -> ${jpgName}`);
  const refBuffer = await fs.readFile(mobileRef);
  const generated = await generateImageWithCodexAppServer({
    prompt: buildPrompt(section) + PC_INSTRUCTION + (RECOMPOSE_NOTES[section.id] || ""),
    sectionId: section.id,
    imageName: jpgName,
    refImages: [{ name: `${section.id}-mobile-reference.jpg`, buffer: refBuffer }],
    cwd: repoRoot,
    taskType: "section",
  });

  if (!generated.configured) {
    throw new Error(generated.message || "Codex app-server image generation is not configured.");
  }

  // Write raw PNG; a separate python step converts to jpg to match the project's file convention.
  await fs.writeFile(outPath.replace(/\.jpg$/i, ".raw.png"), generated.buffer);
  console.log(`done ${section.id}`);
}

console.log(`Generated ${targets.length} PC sections.`);
