function buildPrompt({ scene, composition, mood, orientation }) {
  const aspect =
    orientation === "pc"
      ? "アスペクト比 3:2 の横長構図。横方向に十分な余白を持たせ、被写体は画面内でやや中央〜左寄りに配置し、右側にも自然な余白を残す。"
      : "アスペクト比 4:5 の縦長構図。縦方向に十分な余白（頭上・足元）を持たせ、被写体を画面中央〜やや上寄りに配置する。";

  return [
    "あなたはトップクラスの企業コーポレートサイト向けフォトグラファーです。",
    `目的：日本の物流・運送企業「ツナグ物流株式会社」のコーポレートサイト用写真を1枚生成する。`,
    "────────────────",
    "【最重要ルール】",
    "・実写と見紛うフォトリアルな1枚の写真として生成する（イラスト・3DCG・アニメ調は禁止）",
    "・画像内にテキスト・ロゴ・文字・数字・透かしは一切入れない",
    "・特定の実在企業のロゴ・商標・車両デザインを再現しない（架空の一般的な配送トラック・作業服とする）",
    "────────────────",
    `【シーン】\n${scene}`,
    `【構図】\n${composition}\n${aspect}`,
    `【雰囲気・光】\n${mood}`,
    `【スタイル共通ルール】\n${STYLE_BASE}`,
    "【出力条件】",
    "・そのままコーポレートサイトの写真素材として使える品質",
    "・スマートフォン・PCどちらで見ても被写体が判別しやすい",
  ].join("\n\n");
}

